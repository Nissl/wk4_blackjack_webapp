require 'rubygems'
require 'sinatra'
require 'pry'

set :sessions, true

module Constants
  MAX_SCORE = 21
  DEALER_CUTOFF = 17
end

helpers do
  include Constants

  def setup_game
    deck = []
    values = %w(2 3 4 5 6 7 8 9 10 Jack Queen King Ace)
    suits = %w(Clubs Diamonds Hearts Spades)
    session[:deck] = values.product(suits).shuffle!
    session[:user_cards] = [session[:deck].pop]
    session[:dealer_cards] = [session[:deck].pop]
    session[:user_cards] << session[:deck].pop
    session[:dealer_cards] << session[:deck].pop
    session[:dealer_hitting] = false
  end

  def score(hand)
    total = 0
    vals = hand.map { |card| card[0] }
    vals.each do |val|
      if val == 'Ace'
        total += 11
      else
        total += (val.to_i == 0 ? 10 : val.to_i)
      end
    end

    vals.select { |val| val == 'Ace' }.count.times do
      total -= 10 if total > Constants::MAX_SCORE
    end

    total
  end

  def card_route(card)
    "/images/cards/#{card[1].downcase}_#{card[0].downcase}.jpg"
  end

  def user_wins
    session[:user_money] += session[:user_bet]
    @user_wins = true
  end

  def user_loses
    session[:user_money] -= session[:user_bet]
    @user_loses = true
  end

  def user_draws
    @game_draw = true
  end

  def resolve_game
    if score(session[:dealer_cards]) > Constants::MAX_SCORE
      user_wins
      @dealer_busts = true
    elsif score(session[:dealer_cards]) < score(session[:user_cards])
      user_wins
    elsif score(session[:dealer_cards]) == score(session[:user_cards])
      user_draws
    else
      user_loses
    end
  end
end

get '/' do
  if session[:user_name]
    redirect '/get_money'
  else
    redirect '/get_name'
  end
end

get '/change_player' do
  session.clear
  redirect '/'
end

get '/get_name' do
  erb :get_name
end

post '/set_name' do
  if params[:user_name].empty?
    @error = "You need to enter a name!"
    halt erb(:get_name)
  else
    session[:user_name] = params[:user_name]
    redirect '/get_money'
  end
end

get '/get_money' do
  erb :get_money
end

post '/set_money' do
  unless params[:money].to_i > 0
      @error = "You must buy in to play!"
      erb(:get_money)
  else
    session[:user_money] = params[:money].to_i
    redirect '/get_bet'
  end
end

get '/get_bet' do
  unless session[:user_money]
    redirect '/get_money'
  end
  erb :get_bet
end

post '/set_bet' do
  if params[:bet].empty?
    @error = "You must bet to play!"
    halt erb(:get_bet)
  elsif params[:bet].to_i == 0
    @error = "No bet input"
    halt erb(:get_bet)
  elsif params[:bet].to_i > session[:user_money]
    halt erb(:bad_bet)
  else
    session[:user_bet] = params[:bet].to_i
    setup_game
    redirect '/game'
  end
end

get '/game' do
  #Check for blackjacks
  if (score(session[:user_cards]) == Constants::MAX_SCORE && 
      session[:user_cards].length == 2)
    @blackjack = true
    if score(session[:dealer_cards]) == Constants::MAX_SCORE
      user_draws
    else
      user_wins
      # Pay out blackjack win 3 / 2 by adding extra 1 / 2
      session[:user_money] += session[:user_bet] / 2
    end
  elsif (score(session[:dealer_cards]) == Constants::MAX_SCORE && 
    session[:dealer_cards].length == 2)
    user_loses
    @blackjack = true
  end

  erb :game
end

post '/hit' do
  session[:user_cards] << session[:deck].pop

   # If the user busted, show results
  if score(session[:user_cards]) > Constants::MAX_SCORE
    user_loses
    @user_busts = true
  end
  
  erb :game, layout: false
end

post '/stay' do
  if score(session[:dealer_cards]) < Constants::DEALER_CUTOFF
    session[:dealer_hitting] = true 
  else
    resolve_game
  end

  erb :game, layout: false
end

post '/dealer_hit' do
  session[:dealer_cards] << session[:deck].pop

  if score(session[:dealer_cards]) >= Constants::DEALER_CUTOFF
    session[:dealer_hitting] = false
    resolve_game
  end

  erb :game, layout: false
end

post '/double_down' do
  session[:user_bet] = session[:user_bet] * 2

  # You draw one and only one card after doubling down!
  session[:user_cards] << session[:deck].pop
  session[:dealer_hitting] = true 

  erb :game, layout: false
end

post '/quit' do
  erb :quit
end

